export function buildDesktopContracts() {
  return contractDefinitions.map((definition) => {
    const rustTargets = definition.rust.map((target) => ({
      file: target.file,
      struct: target.struct,
      fields: parseRustStructFields(target.file, target.struct),
      id: target.id,
    }));
    const fieldsById = new Map(rustTargets.map((target) => [target.id, target.fields]));

    const contract = {
      contract: definition.contract,
      version: definition.version,
      rust: rustTargets.map(({ id: _id, ...target }) => target),
      typescript: definition.typescript.map((target) => ({
        file: target.file,
        type: target.type,
        fields: fieldsFromTarget(fieldsById, target),
      })),
    };

    if (definition.requiredText) {
      contract.requiredText = definition.requiredText;
    }
    if (definition.notes) {
      contract.notes = definition.notes;
    }

    return {
      output: definition.output,
      contract,
    };
  });
}

export function contractOutputPath(output) {
  return path.join(desktopRoot, output);
}

export function formatContractJson(contract) {
  return `${JSON.stringify(contract, null, 2)}\n`;
}

export function readDesktopFile(relativePath) {
  return fs.readFileSync(path.resolve(desktopRoot, relativePath), "utf8");
}

export function parseRustStructFields(relativePath, structName) {
  const source = readDesktopFile(relativePath);
  const body = extractBraceBody(
    source,
    new RegExp(`pub\\s+struct\\s+${escapeRegex(structName)}\\b`),
    `${relativePath}:${structName}`,
  );
  const fields = [];
  for (const line of body.split(/\r?\n/)) {
    const match = line.match(/^\s*pub\s+([A-Za-z_][A-Za-z0-9_]*)\s*:/);
    if (match) {
      fields.push(match[1]);
    }
  }
  if (fields.length === 0) {
    throw new Error(`No public fields found for ${structName} in ${relativePath}`);
  }
  return fields;
}

export function parseTypescriptObjectFields(relativePath, typeName) {
  const source = readDesktopFile(relativePath);
  const body = extractBraceBody(
    source,
    new RegExp(`export\\s+type\\s+${escapeRegex(typeName)}\\s*=`),
    `${relativePath}:${typeName}`,
  );
  const fields = [];
  let depth = 0;
  for (const line of body.split(/\r?\n/)) {
    if (depth === 0) {
      const match = line.match(/^\s*(?:readonly\s+)?([A-Za-z_$][A-Za-z0-9_$]*)\??\s*:/);
      if (match) {
        fields.push(match[1]);
      }
    }
    depth += countChar(line, "{") - countChar(line, "}");
  }
  if (fields.length === 0) {
    throw new Error(`No fields found for ${typeName} in ${relativePath}`);
  }
  return fields;
}

function fieldsFromTarget(fieldsById, target) {
  const fields = target.fields ?? fieldsById.get(target.fieldsFrom);
  if (!fields) {
    throw new Error(`Unknown fields source ${target.fieldsFrom} for ${target.type}`);
  }
  if (target.transform === "camel") {
    return fields.map(toCamelCase);
  }
  return [...fields];
}

function extractBraceBody(source, headerPattern, label) {
  const match = headerPattern.exec(source);
  if (!match) {
    throw new Error(`Could not find ${label}`);
  }
  const openIndex = source.indexOf("{", match.index + match[0].length);
  if (openIndex < 0) {
    throw new Error(`Could not find opening brace for ${label}`);
  }

  let depth = 0;
  for (let index = openIndex; index < source.length; index += 1) {
    const char = source[index];
    if (char === "{") {
      depth += 1;
    } else if (char === "}") {
      depth -= 1;
      if (depth === 0) {
        return source.slice(openIndex + 1, index);
      }
    }
  }

  throw new Error(`Could not find closing brace for ${label}`);
}

function toCamelCase(value) {
  return value.replace(/_([a-z0-9])/g, (_match, char) => char.toUpperCase());
}

function escapeRegex(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function countChar(value, char) {
  let count = 0;
  for (const current of value) {
    if (current === char) {
      count += 1;
    }
  }
  return count;
}
